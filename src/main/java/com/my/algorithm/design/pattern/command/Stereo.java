package com.my.algorithm.design.pattern.command;

public class Stereo {

    public void stereoOn() {
        System.out.println("stereo On");
    }

    public void stereoOff() {
        System.out.println("stereo Off");
    }

    public void stereoWithCD() {
        System.out.println("stereo play CD ");
    }

    public void stereoWithDVD() {
        System.out.println("stereo play DVD");
    }
}
